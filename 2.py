x1 = int(input("Введите первое число: "))
x2 = int(input("Введите второе число: "))
x3 = int(input("Введите третье число: "))
f = int(input("1 - Максимум\n2 - Минимум\n3 - Среднеарифмитическое\n"))
if f == 1:
    print(f"Сумма {max(x1,x2,x3)}")
elif f == 2:
    print(f"Разность {min(x1,x2,x3)}")
elif f == 3:
    print(f"Среднеарифмитическое {(x1 + x2 + x3) / 3}")